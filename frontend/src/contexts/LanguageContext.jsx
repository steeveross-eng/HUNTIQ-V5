/**
 * LanguageProvider - BLOC 3 Optimized
 * Added useMemo for context value to prevent unnecessary re-renders
 */
export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState(() => {
    // Check localStorage first
    const saved = localStorage.getItem('bionic_language');
    if (saved) return saved;
    
    // Then check browser language
    const browserLang = navigator.language.toLowerCase();
    if (browserLang.startsWith('fr')) return 'fr';
    if (browserLang.startsWith('en')) return 'en';
    
    // Default to French
    return 'fr';
  });

  useEffect(() => {
    localStorage.setItem('bionic_language', language);
    document.documentElement.lang = language;
  }, [language]);

  const toggleLanguage = useCallback(() => {
    setLanguage(prev => prev === 'fr' ? 'en' : 'fr');
  }, []);

  // BLOC 3: Memoized translation function
  const t = useCallback((key) => {
    return TRANSLATIONS[language][key] || key;
  }, [language]);

  // BLOC 3: Memoized brand object
  const brand = useMemo(() => BRAND_NAMES[language], [language]);
  
  // BLOC 3: Memoized translations reference
  const translations = useMemo(() => TRANSLATIONS[language], [language]);

  // BLOC 3: Memoized context value to prevent re-renders
  const contextValue = useMemo(() => ({ 
    language, 
    setLanguage, 
    toggleLanguage, 
    t, 
    brand,
    translations
  }), [language, setLanguage, toggleLanguage, t, brand, translations]);

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  );
};

// Hook
export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

// Language Switcher Component - Sliding Toggle
export const LanguageSwitcher = ({ className = "" }) => {
  const { language, setLanguage } = useLanguage();
  
  const handleToggle = () => {
    setLanguage(language === 'fr' ? 'en' : 'fr');
  };
  
  return (
    <div 
      className={`flex items-center gap-2 ${className}`}
      data-testid="language-switcher"
    >
      {/* FR Label */}
      <span 
        className={`text-sm font-medium transition-colors cursor-pointer ${
          language === 'fr' ? 'text-[#f5a623]' : 'text-gray-500 hover:text-gray-400'
        }`}
        onClick={() => setLanguage('fr')}
      >
        FR
      </span>
      
      {/* Toggle Slider */}
      <button
        onClick={handleToggle}
        className="relative w-14 h-7 rounded-full bg-card border border-border transition-colors duration-300 hover:border-[#f5a623]/50"
        aria-label="Toggle language"
        data-testid="language-toggle"
      >
        {/* Slider Track Background */}
        <div className="absolute inset-1 rounded-full bg-background" />
        
        {/* Sliding Knob with Flag */}
        <div 
          className={`absolute top-1 w-5 h-5 rounded-full bg-[#f5a623] shadow-lg flex items-center justify-center transition-all duration-300 ease-in-out ${
            language === 'fr' ? 'left-1' : 'left-8'
          }`}
        >
          <span className="text-xs">
            {language === 'fr' ? '🇫🇷' : '🇬🇧'}
          </span>
        </div>
      </button>
      
      {/* EN Label */}
      <span 
        className={`text-sm font-medium transition-colors cursor-pointer ${
          language === 'en' ? 'text-[#f5a623]' : 'text-gray-500 hover:text-gray-400'
        }`}
        onClick={() => setLanguage('en')}
      >
        EN
      </span>
    </div>
  );
};

